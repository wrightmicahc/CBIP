                                  FCID2018,
                                  ID, 
                                  Silvicultural_Treatment,
                                  Burn_Type,
                                  Fraction_Piled,
                                  Fraction_Scattered,
                                  Biomass_Collection, 
                                  Pulp_Market = Pulp_Market,
                                  Secondary_Burn,
                                  Year,
                                  total_except_pile_char,
                                  total_except_pile_CH4, 
                                  total_except_pile_CO, 
                                  total_except_pile_CO2,
                                  total_except_pile_NOx, 
                                  total_except_pile_PM10, 
