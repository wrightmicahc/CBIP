                                  total_except_pile_SO2, 
                                  total_except_pile_VOC,
                                  total_pile_clean_PM10, 
                                  total_pile_vdirty_PM10,
                                  total_pile_clean_PM2.5,
                                  total_pile_vdirty_PM2.5, 
                                  total_pile_CH4,
                                  total_pile_CO,
                                  total_pile_CO2,              
                                  total_pile_NOx,
                                  total_pile_SO2,
                                  total_pile_VOC,
                                  pile_char,
                                  char_fwd_residue,
                                  char_cwd_residue,
                                  total_duff_exposed,
                                  total_foliage_exposed,
                                  total_fwd_exposed,
                                  total_cwd_exposed,
                                  total_fuel_consumed, 
                                  total_pile_consumed,
                                  total_duff_consumed,
                                  total_foliage_consumed,
